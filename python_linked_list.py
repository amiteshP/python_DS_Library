#We are going to create an unordered linked list in this program using python


#To kick off the process first we need to create a "node" class which, obviously, represents our node.

# The Node class is self suffiecient to perform any operations we might wish to perform on any node in the list.

class Node:
    def __init__(self,initdata):
        self.data = initdata
        self.next = None

    def getData(self):
        return self.data

    def getNext(self):
        return self.next

    def setData(self,newdata):
        self.data = newdata

    def setNext(self,newnext):
        self.next = newnext


class UnorderedList:

    def __init__(self):
        self.head = None
   
    #check if empty
    def isEmpty(self):
        return self.head == None
    
    #add a node
    def add(self,item):
        temp = Node(item)
        temp.setNext(self.head)
        self.head = temp

    #size of list
    def size(self):
        current = self.head
        count = 0
        while current != None:
            count = count + 1
            current = current.getNext()

        return count
    
    #search for a node
    def search(self,item):
        current = self.head
        found = False
        while current != None and not found:
            if current.getData() == item:
                found = True
            else:
                current = current.getNext()

        return found

    #remove a node
    def remove(self,item):
        current = self.head
        previous = None
        found = False
        while not found:
            if current.getData() == item:
                found = True
            else:
                previous = current
                current = current.getNext()

        if previous == None:
            self.head = current.getNext()
        else:
            previous.setNext(current.getNext())

#Initialize the list//create an object
mylist = UnorderedList()

